#include <unistd.h>

void	ft_putstr(char *str)
{
	while (*str != '\0')
	{
		write (1, str, 1);
		str++;
	}
}

int	main(void)
{
	char	*point;

	point = "Bulba bulba bulbasauro";
	ft_putstr(point);
	return (0);
}
